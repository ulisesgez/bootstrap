/*
Bootstrap:
Framework CSS para desarrollar sitios web responsive (adaptables) y mobile-first.

Framework:
Estructura (base) que puedes usar para desarrollar tu aplicación o sitio web de
forma más rápida y eficiente.

Framework CSS:
Clases que nos permiten personalizar el estilo de los elementos HTML de nuestro
sitio web.

Adaptable:
Que se adapta a distintos dispositivos en base a su tamaño y orientación.
(en desarrollo web)

Grid de Bootstrap:
Conjunto de contenedores, filas y columnas que definen como de va a presentar y
a alinear el contenido. Un contenedor puede tener varias filas y una fila puede
tener varias columnas. 

Contenedor:
Elemento HTML que permite agrupar otros elementos HTML.

Breakpoints:
Puntos de quiebre que definen el tamaño de la pantalla en el que se va a
adaptar el contenido.
Dimensiones (ancho) a partir de la cual podemos cambiar el estilo o la estructura
de la pagina web.
- Extra small: <576px (celulares) --> xs
- Small: >=576px (tablets) --> sm
- Medium: >=768px (laptops) --> md
- Large: >=992px (desktops) --> lg
- Extra large: >=1200px (desktops grandes) --> xl
- Extra extra large: >=1400px (televisores) --> xxl
*/