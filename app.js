/*
Bootstrap:
Framework CSS para desarrollar sitios web responsive (adaptables) y mobile-first.

Framework:
Estructura (base) que puedes usar para desarrollar tu aplicación o sitio web de
forma más rápida y eficiente.

Framework CSS:
Clases que nos permiten personalizar el estilo de los elementos HTML de nuestro
sitio web.

Adaptable:
Que se adapta a distintos dispositivos en base a su tamaño y orientación.
(en desarrollo web)

Grid de Bootstrap:
Conjunto de contenedores, filas y columnas que definen como de va a presentar y
a alinear el contenido. Un contenedor puede tener varias filas y una fila puede
tener varias columnas. 

Contenedor:
Elemento HTML que permite agrupar otros elementos HTML.

Breakpoints:
Puntos de quiebre que definen el tamaño de la pantalla en el que se va a
adaptar el contenido.
Dimensiones (ancho) a partir de la cual podemos cambiar el estilo o la estructura
de la pagina web.
- Extra small: <576px (celulares) --> xs
- Small: >=576px (tablets) --> sm
- Medium: >=768px (laptops) --> md
- Large: >=992px (desktops) --> lg
- Extra large: >=1200px (desktops grandes) --> xl
- Extra extra large: >=1400px (televisores) --> xxl

Componentes:
Elementos HTML reutilizable que ya viene con un estilo predeterminado
y que podemos usar en nuestro sitio web, paginas o aplicaciones.

Flexbox (Flexible Box Layout):
Permite que los elementos responsivos ubicados dentro de un
contenedor se distribuyan automáticamente en base al tamaño del dispositivo.
Bootstrap incluye clases que podemos usar para trabajar con Flexbox
más fácilmente.

Flex-direction:
Establece el eje principal del contenedor, la dirección en la cual
se van a colocar los elementos dentro del contenedor.
- row: horizontal (por defecto)
- column: vertical
- row-reverse: horizontal invertido
- column-reverse: vertical invertido

Justify-content:
Define como se distribuyen los elementos en el eje principal del contenedor.
- flex-start: al inicio
- flex-end: al final
- center: al centro
- space-between: espacio entre los elementos
- space-around: espacio alrededor de los elementos
- space-evenly: espacio entre los elementos y alrededor de los elementos


*/